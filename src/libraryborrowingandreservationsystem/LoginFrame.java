    private void setupPlaceholders() {
    // --- Setup User ID Hint ---
    jTextField1.setText("Enter User ID");
    jTextField1.setForeground(java.awt.Color.GRAY);
    jTextField1.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            if (jTextField1.getText().equals("Enter User ID")) {
                jTextField1.setText("");
                jTextField1.setForeground(java.awt.Color.BLACK);
            }
        }
        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            if (jTextField1.getText().isEmpty()) {
                jTextField1.setForeground(java.awt.Color.GRAY);
                jTextField1.setText("Enter User ID");
            }
        }
    });

    // --- Setup Password Hint ---
    jPasswordField1.setEchoChar((char) 0); 
    jPasswordField1.setText("Enter Password");
    jPasswordField1.setForeground(java.awt.Color.GRAY);
    jPasswordField1.addFocusListener(new java.awt.event.FocusAdapter() {
        @Override
        public void focusGained(java.awt.event.FocusEvent evt) {
            if (String.valueOf(jPasswordField1.getPassword()).equals("Enter Password")) {
                jPasswordField1.setText("");
                jPasswordField1.setForeground(java.awt.Color.BLACK);
                jPasswordField1.setEchoChar('*'); 
            }
        }
        @Override
        public void focusLost(java.awt.event.FocusEvent evt) {
            if (String.valueOf(jPasswordField1.getPassword()).isEmpty()) {
                jPasswordField1.setForeground(java.awt.Color.GRAY);
                jPasswordField1.setEchoChar((char) 0); 
                jPasswordField1.setText("Enter Password");
            }
        }
    });
}

