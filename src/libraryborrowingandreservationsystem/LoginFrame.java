    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String userID = jTextField1.getText();
    String password = String.valueOf(jPasswordField1.getPassword());

    // 2. Prevent submission of the placeholder text
    if (userID.equals("Enter User ID") || password.equals("Enter Password")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter both your User ID and Password.", "Input Error", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 3. Check the database using your existing manager
    UserDatabaseManager dbManager = new UserDatabaseManager();
    User loggedInUser = dbManager.authenticateUser(userID, password);

    // 4. Validate and Route
    if (loggedInUser != null) {
        if (loggedInUser instanceof Librarian) {
            javax.swing.JOptionPane.showMessageDialog(this, "Welcome back, Librarian " + loggedInUser.getName() + "!");
            // new LibrarianHomeFrame((Librarian) loggedInUser).setVisible(true); 
        } else if (loggedInUser instanceof Students) {
            javax.swing.JOptionPane.showMessageDialog(this, "Welcome back, " + loggedInUser.getName() + "!");
            new StudentDashboard((Students) loggedInUser).setVisible(true);
        }
        this.dispose(); 
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid User ID or Password.", "Login Failed", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

